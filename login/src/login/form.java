    private void cnfpdFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_cnfpdFocusLost
        if(pd.getText().trim().isEmpty())
        {
            l5.setText("Field is Empty!!");
            jButton1.setEnabled(false);
            cnfpd.setEnabled(false);
        }
        else if(!(pd.getText()).equals(cnfpd.getText()))
        {
            l5.setText("Password do not match!!");
            jButton1.setEnabled(false);
            cnfpd.setEnabled(false);
        }
        else
        {
            l5.setText("Password Match!!");
            jButton1.setEnabled(true);
        }
    }//GEN-LAST:event_cnfpdFocusLost
        
